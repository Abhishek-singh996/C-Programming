#include<iostream>
using namespace std;

int main(){
    int a = 1,b = 2;
    cout<< "True value of a and b is: "<<a <<'\n' <<b<<endl;
    cout<< "Value of sum is : "<<(a+b)<<endl;
    cout<< "Value of subtraction is : "<<(a-b)<<endl;
    cout<< "Value of division operator is : "<<(a/b)<<endl;
    cout<< "Value of multiplication is : "<<(a*b)<<endl;
    cout<< "check less than : "<<(a<b)<<endl;
    cout<< "check greater than : "<<(a>b)<<endl; 
    cout<< "check greater than : "<<(a>b)<<endl;
    cout<< "check less than or equal to : "<<(a<=b)<<endl;
    cout<< "check greater than or equal to : "<<(a>=b)<<endl;
    cout<< "value of a++ : "<<(a++)<<endl;
    cout<< "value of a-- : "<<(a--)<<endl;
    cout<< "value of --a : "<<(--a)<<endl;
    cout<< "value of ++a : "<<(++a)<<endl;
    cout<< "value of b++ : "<<(b++)<<endl;
    cout<< "value of b-- : "<<(b--)<<endl;
    cout<< "value of ++b : "<<(++b)<<endl;
    cout<< "value of --b : "<<(--b)<<endl;
    // logical operators
    cout<<"The value of logical operator is : "<<((a==b) && (a!=b))<<endl;


}

