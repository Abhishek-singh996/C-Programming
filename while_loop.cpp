#include <iostream>
using namespace std;

int main() {
    // do-while loop
  int i = 1;
  do{
    cout <<"table of 2 is: "<<"2 X "<<i<<" = "<<  2*i << endl;
    i++;
  }
  while (i <= 10);
  //while loop
  int j = 1;
  while (j <= 10){
    cout <<"table of 2 is: "<<"2 X "<<j<<" = "<<  2*j << endl;
    j++;
  }
  return 0;
}
